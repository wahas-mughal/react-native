class Car {
    constructor(carId, title, model, rent, fuelAverage, coverImage, description){
        this.carId = carId;
        this.title = title;
        this.model = model;
        this.rent = rent;
        this.fuelAverage = fuelAverage;
        this.coverImage = coverImage;
        this.description = description;
    }
}

export default Car;